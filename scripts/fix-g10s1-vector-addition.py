#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
إصلاح نصوص شرح الدرس الثاني في g10s1.ts (جمع المتجهات وطرحها)
النهج: تقسيم النص إلى عناصر متعددة - نص عربي خالص + معادلات منفصلة في سطور
"""

from pathlib import Path

FILE = Path("/home/z/my-project/src/lib/physics/g10s1.ts")

src = FILE.read_text(encoding="utf-8")

# البحث عن بداية الكتلة (السطر الذي يبدأ بـ "جمع المتجهات يختلف")
start_marker = '    "جمع المتجهات يختلف'
start_idx = src.find(start_marker)
assert start_idx >= 0, "لم يُعثر على بداية الكتلة"

# ابحث عن نهاية الكتلة: السطر الذي يبدأ بـ "طرح المتجهات يتم"
end_marker_idx = src.find('    "طرح المتجهات يتم', start_idx)
assert end_marker_idx >= 0, "لم يُعثر على نهاية الكتلة"

# ابحث عن نهاية السطر الأخير
end_of_line = src.find('\n', end_marker_idx)
assert end_of_line >= 0

# استخراج الكتلة القديمة
old_block = src[start_idx:end_of_line]

print("=== الكتلة القديمة ===")
print(repr(old_block[:200]))
print("...")
print(repr(old_block[-100:]))
print(f"الطول: {len(old_block)}")

# الكتلة الجديدة - نص عربي خالص + معادلات في سطور مستقلة
new_block = '''    "جمع المتجهات يختلف عن جمع الأرقام العادية لأن المتجهات لها اتجاهات. يمكن جمعها بطريقتين: بيانيًّا برسم الأشعة، ورياضيًّا بالمركبات.",
    "قاعدة المثلث: لجمع متجهين، نضع بداية المتجه الثاني عند نهاية المتجه الأول، والمحصلة تساوي المتجه من بداية المتجه الأول إلى نهاية المتجه الثاني. تستخدم عندما تكون المتجهات متتالية.",
    "\\\\vec{A} + \\\\vec{B} = \\\\vec{R}",
    "قاعدة متوازي الأضلاع: لجمع متجهين يبدآن من نقطة واحدة، نكمل متوازي الأضلاع، والمحصلة هي القطر من نقطة البداية. تستخدم عندما تكون المتجهات متزامنة.",
    "الطريقة الرياضية: نحلل كل متجه إلى مركباته الأفقية والرأسية، ثم نجمع المركبات الأفقية معًا والرأسية معًا.",
    "R_x = A_x + B_x",
    "R_y = A_y + B_y",
    "المحصلة تُحسب بنظرية فيثاغورس:",
    "|R| = \\\\sqrt{R_x^2 + R_y^2}",
    "واتجاهها يُحسب بدالة الظل العكسية:",
    "\\\\theta = \\\\tan^{-1}\\\\left(\\\\dfrac{R_y}{R_x}\\\\right)",
    "طرح المتجهات يتم بجمع المتجه المعاكس: المتجه المعاكس له نفس مقدار المتجه الأصلي لكن باتجاه معاكس.",
    "\\\\vec{A} - \\\\vec{B} = \\\\vec{A} + (-\\\\vec{B})",'''

# استبدال
new_src = src[:start_idx] + new_block + src[end_of_line:]

FILE.write_text(new_src, encoding="utf-8")
print("\n✓ تم تحديث g10s1.ts بنجاح")
print(f"  حجم الملف قبل: {len(src)} حرف")
print(f"  حجم الملف بعد: {len(new_src)} حرف")

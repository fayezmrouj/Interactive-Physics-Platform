// احصل على اقتباس اليوم - يتغيّر يوميًا بناءً على رقم اليوم في السنة
export function getDailyQuote(date: Date = new Date()): Quote {
  const dayOfYear = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000
  );
  const idx = dayOfYear % PHYSICS_QUOTES.length;
  return PHYSICS_QUOTES[idx];
}

// اقتباس عشوائي (للتنويع عند كل تحميل)
export function getRandomQuote(): Quote {
  return PHYSICS_QUOTES[Math.floor(Math.random() * PHYSICS_QUOTES.length)];
}
